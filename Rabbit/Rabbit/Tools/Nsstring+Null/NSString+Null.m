+ (BOOL)stringIsEmpty:(NSString *)string
{
    if(!string || [string isEqual:[NSNull null]] || ![string isKindOfClass:[NSString class]])
    {
        return YES;
    }
    
    if (0 == [string length])
    {
        return YES;
    }
    else
    {
        string = [string stringByTrimmingCharactersInSet:[NSCharacterSet whitespaceAndNewlineCharacterSet]];
        if(0 == [string length])
        {
            return YES;
        }
    }
    
    return NO;
}


+ (BOOL)stringIsInvalid:(NSString *)string
{
    if(!string || [string isEqual:[NSNull null]] || ![string isKindOfClass:[NSString class]])
    {
        return YES;
    }
    
    return NO;
}


- (BOOL)isValid
{
    return ![NSString stringIsEmpty:self];
}


- (BOOL)isValidUrl
{
    if([NSString stringIsEmpty:self])
    {
        return NO;
    }
    
    NSURL *url = [NSURL URLWithString:self];
    if(url && url.scheme && url.host)
    {
        return YES;
    }
    
    return NO;
}

+ (BOOL)isValidateMobile:(NSString *)mobile
{
    //手机号以13， 15，18开头，八个 \d 数字字符
    NSString *phoneRegex = @"^((13[0-9])|(14[0-9])|(15[0-9])|(17[0-9])|(18[0-9]))\\d{8}$";
    // NSString *phoneRegex = @"^((13[0-9])|(15[^4,\\D])|(18[0,0-9]))\\d{8}$";
    NSPredicate *phoneTest = [NSPredicate predicateWithFormat:@"SELF MATCHES %@",phoneRegex];
    //    NSLog(@"phoneTest is %@",phoneTest);
    return [phoneTest evaluateWithObject:mobile];
}

